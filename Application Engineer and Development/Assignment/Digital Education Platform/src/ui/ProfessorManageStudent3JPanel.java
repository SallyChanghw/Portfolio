    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        
        String studentID = txtStudentID.getText();
        if (studentID.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Don't leave the Student ID blank.", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            StudentHistory studentHistory = uni.getStudentHistory(); 
            Student s = studentHistory.findStudent(txtStudentID.getText());
            if(s == null){
                JOptionPane.showMessageDialog(null, "Student ID you entered does not exist.","Warning",JOptionPane.WARNING_MESSAGE);
                txtStudentID.setText("");
            }else{
                ProfessorManageStudent4JPanel panel = new ProfessorManageStudent4JPanel(workArea,uni,s.getStudentID(),selectedCourse);
                workArea.add("ProfessorManageStudent4JPanel",panel);
                CardLayout layout = (CardLayout) workArea.getLayout();
                layout.next(workArea);
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed
    
    private void queryStudentInfo(String professorID, String semester, String selectedCourse) {
        
        //StudentHistory studentHistory = uni.getStudentHistory(); 
        ArrayList<Student> studentHistory = uni.studentHistory.getStudentHistory();
        CourseHistory courseHistory = uni.getCourseHistory(); 
        String courseID,pfUserID;        
        Integer grade;
        DefaultTableModel model = (DefaultTableModel) tblStudentGrade.getModel();
        model.setRowCount(0);
        
        for(Student student : studentHistory){
            ArrayList<Course> registerCourses=student.getRegisterCourses();         

            for(Course c : registerCourses){
                courseID= c.getCourseID();  
                grade=c.getCourseScore();            
                //semester=courseHistory.findCourse(courseID).getSemester();
                //pfUserID=courseHistory.findCourse(courseID).getProfessorUserID();
                if (c.getCourseID().equals(selectedCourse)){
                    Object[] row = new Object[4];
                    row[0] = student.getStudentID();
                    row[1] = student.getStudentName();
                    row[2] = grade;
                    row[3] = c.getAttendencies();
                    model.addRow(row);
                }
            }
        }
    }

