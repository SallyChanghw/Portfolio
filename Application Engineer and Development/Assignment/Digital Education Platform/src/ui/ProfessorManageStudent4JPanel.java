    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if (txtGrade.getText().isBlank() || txtAttendencies.getText().isBlank()) {
           JOptionPane.showMessageDialog(null, "Please fill in the data.","Warning",JOptionPane.WARNING_MESSAGE);
        }else {            
            Integer score=Integer.valueOf(txtGrade.getText());
            Integer attendencies=Integer.valueOf(txtAttendencies.getText());
            student.setCourseScore(selectedCourse, score);
            student.setCourseAttendencies(selectedCourse, attendencies);
            
            // Back to previous UI
            workArea.remove(this);            
            CardLayout layout = (CardLayout) workArea.getLayout();
            layout.previous(workArea);
        }
    }//GEN-LAST:event_btnSaveActionPerformed


