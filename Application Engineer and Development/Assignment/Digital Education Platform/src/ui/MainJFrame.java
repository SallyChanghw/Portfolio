    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        
        String userid=txtUserID.getText();
        String password=paPassword.getText();
        
//        boolean stuseridExists=false;
//        boolean stpasswordExists=false;
//        boolean pruseridExists=false;
//        boolean prpasswordExists=false;

        Student s=uni.getStudentHistory().idPass(userid, password);
        Professor p=uni.getProfessorHistory().idPass(userid, password);

//        Student s=studentHistory.idPass(userid, password);
//        Professor p=professorHistory.idPass(userid, password);
        
        if(s!=null && userid.equals(s.getStudentUserID()) && password.equals(s.getStudentPassword())){
            StudentsWorkAreaJPanel panel= new StudentsWorkAreaJPanel(workArea, s, uni);
            workArea.add("StudentsWorkAreaJPanel",panel);
            CardLayout layout=(CardLayout) workArea.getLayout();
            layout.next(workArea);
            txtUserID.setText("");
            paPassword.setText("");

        }
        
        else if(p!=null && userid.equals(p.getProfessorUserID()) && password.equals(p.getProfessorPassword())){
            ProfessorWorkAreaJPanel panel= new ProfessorWorkAreaJPanel(workArea,p,uni);
            workArea.add("AdminJPanel",panel);
            CardLayout layout=(CardLayout) workArea.getLayout();
            layout.next(workArea);
            txtUserID.setText("");
            paPassword.setText("");
            
        }
        else if(userid.equals("admin") && password.equals("admin")){ 
            AdminDashboardJPanel panel= new AdminDashboardJPanel(workArea,p,s,uni);
            workArea.add("DashboardJPanel",panel);
            CardLayout layout=(CardLayout) workArea.getLayout();
            layout.next(workArea);
            txtUserID.setText("");
            paPassword.setText("");
        }
        else{
            JOptionPane.showMessageDialog(null, "Account hasn't been created!");
            txtUserID.setText("");
            paPassword.setText("");
        }
        
//        ProfessorWorkAreaJPanel panel= new ProfessorWorkAreaJPanel(workArea);
//        workArea.add("ProfessorWorkAreaJPanel",panel);
//        CardLayout layout=(CardLayout) workArea.getLayout();
//        layout.next(workArea);
    }//GEN-LAST:event_btnLoginActionPerformed

