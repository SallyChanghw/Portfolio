     private void queryCourseInfo(String professorUserid, String semester) {
        
//        String courseID;
//        String courseName;       
        ArrayList<Course> courseHistory = uni.courseHistory.getCourseHistory();
        cboxCourse.removeAllItems();
        for(Course c : courseHistory) {
            
            if (c.getProfessorUserID() != null) {
                if (c.getProfessorUserID().equals(professorUserid) &&
                    c.getSemester().equals(semester)    )  {            
                 //courseName= c.getCourseName().toString();
                 //courseID= c.getCourseID().toString();               
                this.cboxCourse.addItem(c.getCourseID() +"-"+c.getCourseName());   
                    }
             }       
        }
    }

