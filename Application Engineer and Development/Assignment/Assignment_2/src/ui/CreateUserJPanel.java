    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        
        String name=txtName.getText();
        String nuid=txtNUID.getText();
        String userid=txtuserID.getText();
        String password=paPassword.getText();
        String status="None";
        
        Person person = history.addPerson();
        person.setName(name);
        person.setNuID(nuid);
        person.setUserID(userid);
        person.setPassword(history.encrypt(password, "a"));
        person.setStatus(status);
        
        JOptionPane.showMessageDialog(null,"User Information successfully created!");
        
        txtName.setText("");
        txtNUID.setText("");
        txtuserID.setText("");
        paPassword.setText("");
    }//GEN-LAST:event_btnSaveActionPerformed

