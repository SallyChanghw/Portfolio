    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        Person result=history.searchPerson(txtNUID.getText());
        if(result==null){
            JOptionPane.showMessageDialog(null,"NUID you entered does not exist","Information",JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            ViewPersonJPanel panel=new ViewPersonJPanel(userProcessContainer,result);
            userProcessContainer.add("ViewPersonJPanel",panel);
            CardLayout layout=(CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);

        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnViewDeatilsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewDeatilsActionPerformed
        // TODO add your handling code here:
        int selectedRow=tblPerson.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null,"Please select a row from table first to view details","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else{
            Person person = (Person) tblPerson.getValueAt(selectedRow, 0);
            ViewPersonJPanel panel=new ViewPersonJPanel(userProcessContainer,person);
            userProcessContainer.add("ViewPersonJPanel",panel);
            CardLayout layout=(CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }

    }//GEN-LAST:event_btnViewDeatilsActionPerformed

    private void btnDeleteAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteAccountActionPerformed
        // TODO add your handling code here:
        int selectedRow=tblPerson.getSelectedRow();
        if(selectedRow>=0){
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null,"Would you like to delete the account details?","Warning",dialogButton);
            if(dialogResult==JOptionPane.YES_NO_OPTION){
                Person person =(Person) tblPerson.getValueAt(selectedRow, 0);
                history.deletePerson(person);
                populateTable();
            }
        }
        else{
            JOptionPane.showMessageDialog(null,"Please select a row from table first","Warning",JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_btnDeleteAccountActionPerformed

