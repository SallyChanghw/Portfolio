    private void populateTable(){
        DefaultTableModel dtm=(DefaultTableModel) tblPerson.getModel();
        dtm.setRowCount(0);
        for(Person person:history.getPersonhistory()){
            Object[] row =new Object[5];
            row[0]=person;
            row[1]=person.getNuID();
            row[2]=person.getUserID();
            row[3]=person.getPassword();
            row[4]=person.getStatus();
            
            dtm.addRow(row);
        }
    }    


