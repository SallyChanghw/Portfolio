    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        
        String username = txtUsername.getText();
        String password = txtPassword.getText();
        
        // Password validation
        boolean isValidPassword = password.length() >= 8 &&
            password.matches(".*[a-z].*") &&
            password.matches(".*[A-Z].*") &&
            password.matches(".*\\d.*");

        
//      Check if any of the fields is empty
        if (username.isEmpty() || password.isEmpty()) {
            
//      Display a message if any field is empty
        JOptionPane.showMessageDialog(this, "Please fill out all the blanks.", "Error", JOptionPane.ERROR_MESSAGE);
        return; // Stop further processing
        }
        else {
            if (!isValidPassword){
            JOptionPane.showMessageDialog(null, "Password must be at least 8 characters long and contain at least one uppercase letter, one lowercase letter, and one digit.");
            txtPassword.setText("");
            }
            else {
    //      Create a new purchasing officer using the DoctorHistory class
            PurchasingOfficer po = hospital.getPurchasingOfficerHistory().addPurchasingRequest();

    //      Create a new UserAccount for the purchasing officer
            UserAccount account = new UserAccount();
            account.setUsername(username);
            account.setPassword(password);
            account.setRole(new PurchasingOfficerRole());
            account.setPurchasingOfficer(po);

    //      Add the new purchasing officer to the PurchasingOfficerHistory
            hospital.getPurchasingOfficerHistory().getPurchasingOfficerList().add(po);

    //      Add the new UserAccount to the UserAccountDirectory
            hospital.getOrganizationDirectory().getOrganizationList().stream()
                .filter(org -> org instanceof StaffOrganization)
                .map(org -> (StaffOrganization) org)
                .findFirst()
                .ifPresent(doctorOrg -> doctorOrg.getUserAccountDirectory().getUserAccountList().add(account));

            clearInputFields();

    //      Display a success message
            JOptionPane.showMessageDialog(this, "User account of doctor has been successfully created!", "Success", JOptionPane.INFORMATION_MESSAGE);
            }
        }
        
//      
    }//GEN-LAST:event_btnCreateActionPerformed

