    private void btnApproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApproveActionPerformed
        // TODO add your handling code here:
        int selectedRow=tblRequest.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null,"Please select a row from table first","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else{

            String date = (String) tblRequest.getValueAt(selectedRow, 0);
            String manufacturerName= (String) tblRequest.getValueAt(selectedRow, 2);
            String productName= (String) tblRequest.getValueAt(selectedRow, 3);
            int quantity=(int) tblRequest.getValueAt(selectedRow, 4);
            int a=0;
            
            for(Manufacturer manufacturer:hospital.manufacturerDirectory.getManufacturerDirectory()){
                if(manufacturerName.equals(manufacturer.getManufactureName())&& productName.equals(manufacturer.getProductName())){
                    int remain=manufacturer.getQuantity();
                    remain=remain-quantity;
                   
                    if(remain<0){
                        a=1;
                        JOptionPane.showMessageDialog(null,"The quantity is insufficient.","Information",JOptionPane.INFORMATION_MESSAGE);
                    }
                    else{
                        manufacturer.setQuantity(remain);
                    }
                    
                }
            }
            
            for(PurchasingOfficer test :hospital.purchasingOfficerHistory.getPurchasingOfficerList()){
                if(date.equals(test.getDate())){
                    if(a==0){
                        test.setStatus("Approved");
                        test.setResultMessage("");
                        JOptionPane.showMessageDialog(null,"The request is Approve now!","Information",JOptionPane.INFORMATION_MESSAGE);
                        purchaseTable();
                        txtBudget.setText(Integer.toString(budget()));
                    }
                    else{
                        test.setStatus("Rejected");
                        test.setResultMessage("The quantity is insufficient.");
                    }

                }
            }
            
            purchaseTable();
            txtBudget.setText(Integer.toString(budget()));
            
        }
    }//GEN-LAST:event_btnApproveActionPerformed

