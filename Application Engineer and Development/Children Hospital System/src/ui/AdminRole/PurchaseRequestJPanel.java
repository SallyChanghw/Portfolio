    public int budget(){
        int origin=1000000;
        int calculate=origin;
        for(PurchasingOfficer purchasingOfficer :hospital.purchasingOfficerHistory.getPurchasingOfficerList()){
            String status=purchasingOfficer.getStatus();

            if(status=="Approved"){
                int tempPrice = purchasingOfficer.getPrice();
                int tempQuantity = purchasingOfficer.getQuantity();
                int tempTotal=tempPrice * tempQuantity;
                calculate=calculate-tempTotal;
                if(calculate<0){
                    JOptionPane.showMessageDialog(null,"Budget Deficit!","Warning",JOptionPane.WARNING_MESSAGE);
                }
            }
        }
        
        return calculate;
    }

