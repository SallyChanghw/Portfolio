    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        
        String username = txtUsername.getText();
        String password = txtPassword.getText();
        
        Manufacturer m = hospital.getManufacturerDirectory().usernamePass(username, password);
      
        if(m!=null && username.equals(m.getUsername()) && password.equals(m.getPassword())){
            ManufacturerWorkAreaJPanel panel= new ManufacturerWorkAreaJPanel(mainWorkArea, m, hospital);
            mainWorkArea.add("ManufacturerWorkAreaJPanel",panel);
            CardLayout layout=(CardLayout) mainWorkArea.getLayout();
            layout.next(mainWorkArea);
        }
        else{
            JOptionPane.showMessageDialog(null, "Invalid User Name/ Password.");
        }
        
        txtUsername.setText("");
        txtPassword.setText("");
    }//GEN-LAST:event_btnLoginActionPerformed

