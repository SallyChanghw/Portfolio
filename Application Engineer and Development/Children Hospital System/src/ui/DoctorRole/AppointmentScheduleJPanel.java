    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        if(txtAvailPatient.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "The information isn't completed! Please check!");
            txtAvailPatient.requestFocusInWindow();
        }  
        else{
          
            String scheduleDay= cboxDate.getSelectedItem().toString();
            String timeSlot=cboxTimeSlot.getSelectedItem().toString();
            
            try {
                int availability=Integer.parseInt(txtAvailPatient.getText());
                String status="Pending";

                AppointmentSchedule schedule=hospital.getAppointmentHistory().newAppSchedule(doctor.getDoctorID(), doctor.getDepartment()
                        , timeSlot, scheduleDay, availability, status);

                populateTable();
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Please enter a valid number in the available number of patients area");
                txtAvailPatient.requestFocusInWindow();
                txtAvailPatient.setText("");
            }
            
        }
        
    }//GEN-LAST:event_btnSubmitActionPerformed

