    public void initData() {
         String datestring;
         cboxDate.removeAllItems();
         cboxTimeSlot.removeAllItems();
         
         //Date date = new Date();
         LocalDateTime today = LocalDateTime.now(); // Today
         for (int i = 0; i < 20; i = i+1 ){
             datestring=String.format("%1$tY-%1$tm-%1$td", today.plusDays(i));
             cboxDate.addItem(datestring);
         }
         
         cboxTimeSlot.addItem("Morning Clinic");
         cboxTimeSlot.addItem("Afternoon Clinic");
         cboxTimeSlot.addItem("Night Clinic");
         
     }
     
    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblScheduleTimeSlot.getModel();

        model.setRowCount(0);

        for (AppointmentSchedule schedule : hospital.getAppointmentHistory().getAppScheduleList()) {
            String doctorID = schedule.getDoctorID();
                       
            if (doctorID.equals(doctor.getDoctorID()) ) {
                Object[] row = new Object[6];
                row[0] = schedule.getScheduleDate();
                row[1] = schedule.getTimeSlot();
                row[2] = schedule.getDepartment();
                row[3] = schedule.getDoctorID();
                row[4] = Integer.toString(schedule.getAvailability());
                row[5] = schedule.getStatus();
                //row[2] = request.getReceiver() == null ? null : request.getReceiver().getEmployee().getName();

                model.addRow(row);
            }
            
        }
    }
