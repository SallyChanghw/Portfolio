    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if(txtSupplierName.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Please check again", "Information", JOptionPane.INFORMATION_MESSAGE);
            
        }
        else{
            supplier.setSupplyName(txtSupplierName.getText());
            JOptionPane.showMessageDialog(this, "Profile successfully updated", "Information", JOptionPane.INFORMATION_MESSAGE);
        
            txtSupplierName.setEnabled(false);
            btnSave.setEnabled(false);
            btnUpdate.setEnabled(true);
            backAction();
            
        }
        
    }//GEN-LAST:event_btnSaveActionPerformed


